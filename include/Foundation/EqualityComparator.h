#ifndef _I_EQUALITYCOMPARATOR_H_
#define _I_EQUALITYCOMPARATOR_H_

namespace I {
	template<typename T> class EqualityComparator 
	{
	public:
		virtual bool operator () (const T& a, const T& b) const {
			return a == b;
		};
	};
};

#endif